    // src/Repository/BookRepository.php

    public function findRandomBooks(int $limit = 8): array
{
    $books = $this->createQueryBuilder('b')
        ->leftJoin('b.authors', 'a')
        ->addSelect('a')
        ->leftJoin('b.categories', 'c')
        ->addSelect('c')
        ->getQuery()
        ->getResult();

    shuffle($books);

    return array_slice($books, 0, $limit);
}


